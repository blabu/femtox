u16 CRC16(BaseSize_t size, byte_ptr msg){
	u08 CRC_H = 0xFF;
	u08 CRC_L = 0xFF;
	for(BaseSize_t i = 0; i<size; i++) {
		u08 dat = msg[i];
		u16 index = CRC_H ^ dat;
		CRC_H = CRC_L ^ tabl_crc_hi[index];
		CRC_L = tabl_crc_lo[index];
	}
	return ((CRC_H << 8) | CRC_L);
}
